// Goéland POC - Actor component (external persons & organizations).
//
// Proto-first definition for ActorService + supporting messages. An Actor is any
// external party the administration interacts with: a physical PERSON or a moral
// ORGANIZATION. Actors are subjects (kind = ACTOR) and are wired into cases,
// documents and things exclusively through typed CoreService relationships
// (CASE_HAS_ACTOR_*, DOCUMENT_AUTHORED_BY_ACTOR, ...), never through fields on the
// actor itself. Roles therefore stay in relationship_type and are added slice by
// slice as Case / Thing land; this file deliberately keeps Actor role-free.
//
// Model informed by the production Goéland (MSSQL) Acteur domain:
//   - Acteur base: IsPhysique -> ActorKind PERSON | ORGANIZATION; IsFromCH,
//     IsActive, Name / NameForSearch.
//   - ActMoral (organization): RaisonSociale + category (DicoActMoralCategory).
//   - ActeurComplement (typed contact & identity bag): phone / email + business
//     identifiers IDE Fédéral, N° TVA, N° débiteur ABACUS, commercial-register link.
// Physical-person register data (ActPhysFromCH) is intentionally NOT modelled here
// beyond an is_ch_register flag + opaque reference — no personal data is carried.
//
// First slice = identity + contacts. Addresses (lien_acteur_adresse) and the full
// 46-role relationship seeding are separate later slices.

